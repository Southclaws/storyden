package pluginbuilder

import (
	"context"
	"errors"
	"fmt"
	"io/fs"
	"regexp"
	"strings"

	"github.com/goccy/go-yaml"
	adktool "google.golang.org/adk/tool"
	"google.golang.org/adk/tool/functiontool"

	"github.com/Southclaws/storyden/app/services/semdex/robot/workspaceprovider"
)

const manifestYAMLFilename = "manifest.yaml"

var workspaceIDPattern = regexp.MustCompile(`^[a-z0-9](?:[a-z0-9-]*[a-z0-9])?$`)

type CreateWorkspaceInput struct {
	PluginID    string `json:"plugin_id" jsonschema:"Stable plugin ID using letters numbers and dashes"`
	Name        string `json:"name" jsonschema:"Human readable plugin name"`
	Description string `json:"description" jsonschema:"Short manifest description"`
	Author      string `json:"author" jsonschema:"Author slug"`
	Overwrite   bool   `json:"overwrite" jsonschema:"Overwrite existing starter files if the workspace already exists"`
}

type WorkspaceInfo struct {
	ID    string `json:"id"`
	Files int    `json:"files"`
}

type WorkspaceResult struct {
	Workspace WorkspaceInfo `json:"workspace"`
}

func (a *Agent) addWorkspaceTools(add toolAdder) error {
	if err := add(functiontool.New(functiontool.Config{
		Name:        "plugin_workspace_create",
		Description: "Create a managed Go plugin workspace with manifest.yaml, go.mod, main.go, and README.md starter files.",
	}, func(ctx adktool.Context, args CreateWorkspaceInput) (WorkspaceResult, error) {
		result, err := a.Create(ctx, args)
		if err != nil {
			return WorkspaceResult{}, err
		}
		return result, nil
	})); err != nil {
		return err
	}

	return add(functiontool.New(functiontool.Config{
		Name:        "plugin_workspace_info",
		Description: "Get managed plugin workspace metadata and file count.",
	}, func(ctx adktool.Context, args struct{}) (WorkspaceResult, error) {
		result, err := a.Info(ctx)
		if err != nil {
			return WorkspaceResult{}, err
		}
		return result, nil
	}))
}

func (a *Agent) Create(ctx context.Context, in CreateWorkspaceInput) (WorkspaceResult, error) {
	id := slugify(in.PluginID)
	if id == "" {
		id = slugify(in.Name)
	}
	if id == "" {
		return WorkspaceResult{}, errors.New("plugin_id or name is required")
	}
	if !workspaceIDPattern.MatchString(id) {
		return WorkspaceResult{}, fmt.Errorf("invalid workspace id %q", id)
	}
	if err := ensurePluginBuildTarget(ctx, id, ""); err != nil {
		return WorkspaceResult{}, err
	}

	name := strings.TrimSpace(in.Name)
	if name == "" {
		name = titleize(id)
	}
	description := strings.TrimSpace(in.Description)
	if description == "" {
		description = "Storyden plugin generated by the plugin builder robot."
	}
	author := slugify(in.Author)
	if author == "" {
		author = "storyden"
	}

	workspace, err := a.Workspace(ctx)
	if err != nil {
		return WorkspaceResult{}, err
	}

	files := map[string]string{
		manifestYAMLFilename: renderManifestYAML(id, name, author, description),
		"go.mod":             renderGoMod(id),
		"main.go":            renderMainGo(),
		"README.md":          renderReadme(name),
	}

	for rel, body := range files {
		if !in.Overwrite {
			if _, err := workspace.ReadFile(ctx, rel, 1); err == nil {
				return WorkspaceResult{}, fmt.Errorf("%s already exists; set overwrite to true to replace starter files", rel)
			} else if !errors.Is(err, fs.ErrNotExist) {
				return WorkspaceResult{}, err
			}
		}
		if _, err := workspace.WriteFile(ctx, rel, []byte(body)); err != nil {
			return WorkspaceResult{}, err
		}
	}

	info, err := a.Info(ctx)
	if err != nil {
		return WorkspaceResult{}, err
	}

	if err := a.setPluginBuildTarget(ctx, pluginBuildTarget{
		Mode:       pluginBuildTargetModeNew,
		ManifestID: id,
	}); err != nil {
		return WorkspaceResult{}, err
	}

	info.Workspace.ID = id
	return info, nil
}

func (a *Agent) Info(ctx context.Context) (WorkspaceResult, error) {
	workspace, err := a.Workspace(ctx)
	if err != nil {
		return WorkspaceResult{}, err
	}

	files, err := workspace.List(ctx, workspaceprovider.ListOptions{MaxFiles: 500})
	if err != nil {
		return WorkspaceResult{}, err
	}

	return WorkspaceResult{
		Workspace: WorkspaceInfo{
			ID:    "active",
			Files: len(files),
		},
	}, nil
}

func renderManifestYAML(id, name, author, description string) string {
	return fmt.Sprintf(`id: %s
name: %s
author: %s
description: %s
version: 0.1.0
command: %s
args: []
events_consumed:
  - EventThreadPublished
`, id, yamlQuote(name), author, yamlQuote(description), yamlQuote(packagedPluginCommand))
}

func yamlQuote(s string) string {
	b, _ := yaml.Marshal(s)
	return strings.TrimSpace(string(b))
}

func renderGoMod(id string) string {
	return fmt.Sprintf(`module storyden.local/plugins/%s

go 1.26.4

require github.com/Southclaws/storyden latest
`, id)
}

func renderMainGo() string {
	return `package main

import (
	"context"
	"fmt"
	"log"
	"os"
	"os/signal"

	"github.com/Southclaws/storyden/lib/plugin/rpc"
	"github.com/Southclaws/storyden/sdk/go/storyden"
)

func main() {
	ctx, cancel := signal.NotifyContext(context.Background(), os.Interrupt)
	defer cancel()

	pl, err := storyden.New(ctx)
	if err != nil {
		log.Fatalf("failed to initialise plugin: %v", err)
	}
	defer func() {
		if err := pl.Shutdown(); err != nil {
			log.Printf("shutdown error: %v", err)
		}
	}()

	pl.OnThreadPublished(func(ctx context.Context, event *rpc.EventThreadPublished) error {
		fmt.Printf("thread published: %+v\n", event)
		return nil
	})

	if err := pl.Run(ctx); err != nil {
		log.Fatalf("plugin stopped: %v", err)
	}
}
`
}

func renderReadme(name string) string {
	return fmt.Sprintf(`# %s

This Storyden plugin workspace was created by the plugin builder robot.

Use the robot tools to edit files, run gofmt, run go vet, run go test, package,
and install the plugin as a supervised Storyden plugin.
`, name)
}
