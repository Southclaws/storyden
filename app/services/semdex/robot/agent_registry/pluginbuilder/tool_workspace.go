const manifestYAMLFilename = "manifest.yaml"

var workspaceIDPattern = regexp.MustCompile(`^[a-z0-9](?:[a-z0-9-]*[a-z0-9])?$`)

type CreateWorkspaceInput struct {
	PluginID    string `json:"plugin_id" jsonschema:"Stable plugin ID using letters numbers and dashes"`
	Name        string `json:"name" jsonschema:"Human readable plugin name"`
	Description string `json:"description" jsonschema:"Short manifest description"`
	Author      string `json:"author" jsonschema:"Author slug"`
	Overwrite   bool   `json:"overwrite" jsonschema:"Overwrite existing starter files if the workspace already exists"`
}

type WorkspaceInfo struct {
	ID    string `json:"id"`
	Files int    `json:"files"`
}

type WorkspaceResult struct {
	Workspace  WorkspaceInfo `json:"workspace"`
	Message    string        `json:"message,omitempty"`
	NextAction string        `json:"next_action,omitempty"`
}

func (a *Agent) addWorkspaceTools(add toolAdder) error {
	if err := add(functiontool.New(functiontool.Config{
		Name:        "plugin_workspace_create",
		Description: "Create the initial managed Go plugin workspace for this chat. Writes manifest.yaml, go.mod, main.go, and README.md starter files with Storyden SDK, live configuration, event-handler, and shutdown patterns. Use only when creating a new plugin in an empty workspace; import existing supervised plugins with plugin_workspace_import_installation instead.",
	}, func(ctx adktool.Context, args CreateWorkspaceInput) (WorkspaceResult, error) {
		result, err := a.Create(ctx, args)
		if err != nil {
			return WorkspaceResult{}, err
		}
		return result, nil
	})); err != nil {
		return err
	}

	return add(functiontool.New(functiontool.Config{
		Name:        "plugin_workspace_info",
		Description: "Inspect whether this chat already has a managed plugin workspace and how many files it currently sees. Use at the start of a task to decide whether to create a new plugin, import an installed plugin, or continue editing the existing plugin. This does not read source content; use plugin_file_list/read/search for files.",
	}, func(ctx adktool.Context, args struct{}) (WorkspaceResult, error) {
		result, err := a.Info(ctx)
		if err != nil {
			return WorkspaceResult{}, err
		}
		return result, nil
	}))
}

func (a *Agent) Create(ctx context.Context, in CreateWorkspaceInput) (WorkspaceResult, error) {
	id := slugify(in.PluginID)
	if id == "" {
		id = slugify(in.Name)
	}
	if id == "" {
		return WorkspaceResult{}, errors.New("plugin_id or name is required")
	}
	if !workspaceIDPattern.MatchString(id) {
		return WorkspaceResult{}, fmt.Errorf("invalid workspace id %q", id)
	}
	if err := ensurePluginBuildTarget(ctx, id, ""); err != nil {
		return WorkspaceResult{}, err
	}

	name := strings.TrimSpace(in.Name)
	if name == "" {
		name = titleize(id)
	}
	description := strings.TrimSpace(in.Description)
	if description == "" {
		description = "Storyden plugin generated by the plugin builder robot."
	}
	author := slugify(in.Author)
	if author == "" {
		author = "storyden"
	}

	workspace, err := a.Workspace(ctx)
	if err != nil {
		return WorkspaceResult{}, err
	}

	files := map[string]string{
		manifestYAMLFilename: renderManifestYAML(id, name, author, description),
		"go.mod":             renderGoMod(id),
		"main.go":            renderMainGo(),
		"README.md":          renderReadme(name),
	}

	for rel, body := range files {
		if !in.Overwrite {
			if _, err := workspace.ReadFile(ctx, rel, 1); err == nil {
				return WorkspaceResult{}, fmt.Errorf("%s already exists; set overwrite to true to replace starter files", rel)
			} else if !errors.Is(err, fs.ErrNotExist) {
				return WorkspaceResult{}, err
			}
		}
		if _, err := workspace.WriteFile(ctx, rel, []byte(body)); err != nil {
			return WorkspaceResult{}, err
		}
	}

	info, err := a.Info(ctx)
	if err != nil {
		return WorkspaceResult{}, err
	}

	if err := a.setPluginBuildTarget(ctx, pluginBuildTarget{
		Mode:       pluginBuildTargetModeNew,
		ManifestID: id,
	}); err != nil {
		return WorkspaceResult{}, err
	}

	info.Workspace.ID = id
	info.Message = "Plugin workspace created with managed Go starter files."
	info.NextAction = "Inspect or edit main.go and manifest.yaml for the requested behavior, then use plugin_validate while iterating and plugin_install when ready."
	return info, nil
}

func (a *Agent) Info(ctx context.Context) (WorkspaceResult, error) {
	workspace, err := a.Workspace(ctx)
	if err != nil {
		return WorkspaceResult{}, err
	}

	files, err := workspace.List(ctx, workspaceprovider.ListOptions{MaxFiles: 500})
	if err != nil {
		return WorkspaceResult{}, err
	}

	return WorkspaceResult{
		Workspace: WorkspaceInfo{
			ID:    "active",
			Files: len(files),
		},
		Message: "Active plugin workspace metadata.",
	}, nil
}

func renderManifestYAML(id, name, author, description string) string {
	return fmt.Sprintf(`id: %s
name: %s
author: %s
description: %s
version: 0.1.0
command: %s
args: []
events_consumed:
  - EventThreadPublished
`, id, yamlQuote(name), author, yamlQuote(description), yamlQuote(packagedPluginCommand))
}

func yamlQuote(s string) string {
	b, _ := yaml.Marshal(s)
	return strings.TrimSpace(string(b))
}

func renderGoMod(id string) string {
	return fmt.Sprintf(`module storyden.local/plugins/%s

go 1.26.4

require github.com/Southclaws/storyden latest
`, id)
}

func renderMainGo() string {
	return `package main

import (
	"context"
	"errors"
	"log/slog"
	"os"
	"os/signal"
	"sync"
	"time"

	"github.com/Southclaws/storyden/lib/plugin/rpc"
	"github.com/Southclaws/storyden/sdk/go/storyden"
)

const (
	initialConfigRetryInterval = 2 * time.Second
	configureTimeout           = 15 * time.Second
)

type pluginConfig struct {
	// Add manifest configuration_schema fields here as this plugin grows.
	// Missing user-provided configuration should leave the plugin running in an
	// unconfigured state; do not exit the process while waiting for UI config.
}

type pluginApp struct {
	plugin *storyden.Plugin
	logger *slog.Logger

	mu         sync.RWMutex
	config     pluginConfig
	configured bool
}

func main() {
	ctx, cancel := signal.NotifyContext(context.Background(), os.Interrupt)
	defer cancel()

	logger := slog.New(slog.NewTextHandler(os.Stdout, &slog.HandlerOptions{Level: slog.LevelInfo}))
	slog.SetDefault(logger)

	pl, err := storyden.New(ctx)
	if err != nil {
		logger.Error("failed to initialise plugin", slog.String("error", err.Error()))
		os.Exit(1)
	}
	defer func() {
		if err := pl.Shutdown(); err != nil && !errors.Is(err, context.Canceled) {
			logger.Warn("plugin shutdown returned error", slog.String("error", err.Error()))
		}
	}()

	app := &pluginApp{
		plugin: pl,
		logger: logger,
	}

	// Configuration is live. A supervised plugin can start before the user has
	// entered settings in the UI; OnConfigure is called later when settings are
	// saved. Keep the process alive and gate only the behaviours that require
	// configuration.
	pl.OnConfigure(app.handleConfigure)

	pl.OnThreadPublished(app.handleThreadPublished)

	go app.syncInitialConfig(ctx)

	if err := pl.Run(ctx); err != nil {
		if errors.Is(err, context.Canceled) {
			return
		}
		logger.Error("plugin stopped", slog.String("error", err.Error()))
		os.Exit(1)
	}
}

func (a *pluginApp) handleConfigure(ctx context.Context, raw map[string]any) error {
	timeoutCtx, cancel := context.WithTimeout(ctx, configureTimeout)
	defer cancel()

	return a.applyConfig(timeoutCtx, raw, true)
}

func (a *pluginApp) syncInitialConfig(ctx context.Context) {
	for {
		select {
		case <-ctx.Done():
			return
		default:
		}

		raw, err := a.plugin.GetConfig(ctx)
		if err != nil {
			a.logger.Debug("initial configuration not available yet", slog.String("error", err.Error()))
			select {
			case <-ctx.Done():
				return
			case <-time.After(initialConfigRetryInterval):
			}
			continue
		}

		if err := a.applyConfig(ctx, raw, false); err != nil {
			a.logger.Warn("stored configuration is invalid", slog.String("error", err.Error()))
		}
		return
	}
}

func (a *pluginApp) applyConfig(ctx context.Context, raw map[string]any, requireComplete bool) error {
	_ = ctx

	cfg, complete, err := parseConfig(raw)
	if err != nil {
		return err
	}
	if !complete {
		a.setUnconfigured()
		a.logger.Info("plugin is waiting for configuration")
		if requireComplete {
			return errors.New("configuration is incomplete")
		}
		return nil
	}

	a.mu.Lock()
	a.config = cfg
	a.configured = true
	a.mu.Unlock()

	a.logger.Info("plugin configuration applied")
	return nil
}

func parseConfig(raw map[string]any) (pluginConfig, bool, error) {
	_ = raw

	return pluginConfig{}, true, nil
}

func (a *pluginApp) setUnconfigured() {
	a.mu.Lock()
	a.config = pluginConfig{}
	a.configured = false
	a.mu.Unlock()
}

func (a *pluginApp) currentConfig() (pluginConfig, bool) {
	a.mu.RLock()
	defer a.mu.RUnlock()

	return a.config, a.configured
}

func (a *pluginApp) handleThreadPublished(ctx context.Context, event *rpc.EventThreadPublished) error {
	_, configured := a.currentConfig()
	if !configured {
		a.logger.Info("thread published event ignored because plugin is not configured", slog.String("thread_id", event.ID.String()))
		return nil
	}

	a.logger.Info("thread published event received", slog.String("thread_id", event.ID.String()))
	return nil
}
`
}

func renderReadme(name string) string {
	return fmt.Sprintf(`# %s

This Storyden plugin workspace was created by the plugin builder robot.

Use the robot tools to edit files, run gofmt, run go vet, run go test, package,
and install the plugin as a supervised Storyden plugin.
`, name)
}
