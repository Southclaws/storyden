package main

import (
	"bytes"
	"encoding/json"
	"fmt"
	"go/format"
	"log"
	"os"
	"sort"
	"strings"
	"text/template"
)

func main() {
	if err := run(); err != nil {
		log.Fatal(err)
	}
}

func run() error {
	if len(os.Args) < 3 {
		return fmt.Errorf("usage: toolbindgen <input.json> <output.go>")
	}
	inputFile := os.Args[1]
	outputFile := os.Args[2]

	data, err := os.ReadFile(inputFile)
	if err != nil {
		return fmt.Errorf("read %s: %w", inputFile, err)
	}

	var schema map[string]any
	if err := json.Unmarshal(data, &schema); err != nil {
		return fmt.Errorf("unmarshal JSON: %w", err)
	}

	defs, _ := schema["definitions"].(map[string]any)

	var suffixes []string
	for name, raw := range defs {
		if !strings.HasPrefix(name, "Tool") || len(name) <= 4 || !isUpper(name[4]) {
			continue
		}
		if strings.HasSuffix(name, "Input") || strings.HasSuffix(name, "Output") {
			continue
		}
		def, _ := raw.(map[string]any)
		if _, hasExt := def["x-storyden-tool"]; !hasExt {
			continue
		}
		suffixes = append(suffixes, strings.TrimPrefix(name, "Tool"))
	}
	sort.Strings(suffixes)

	type entry struct {
		Suffix  string
		VarName string
	}
	entries := make([]entry, len(suffixes))
	for i, s := range suffixes {
		entries[i] = entry{
			Suffix:  s,
			VarName: "tool" + s,
		}
	}

	fmt.Printf("toolbindgen: generating %d tools → %s\n", len(entries), outputFile)

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, entries); err != nil {
		return fmt.Errorf("execute template: %w", err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("format source: %w\n%s", err, buf.String())
	}

	if err := os.WriteFile(outputFile, formatted, 0644); err != nil {
		return fmt.Errorf("write %s: %w", outputFile, err)
	}

	return nil
}

func isUpper(b byte) bool {
	return b >= 'A' && b <= 'Z'
}

var tmpl = template.Must(template.New("bindings").Parse(`// Code generated by toolbindgen. DO NOT EDIT.

package mcp

var (
{{- range .}}
	{{.VarName}} *ToolDefinition
{{- end}}
	toolNamesEnum []any
)

func initAllTools() {
{{- range .}}
	{{.VarName}} = initTool("{{.Suffix}}")
{{- end}}

	names := AllToolNames()
	toolNamesEnum = make([]any, len(names))
	for i, name := range names {
		toolNamesEnum[i] = name
	}
}

// AllToolNames returns the names of all registered tools.
func AllToolNames() []string {
	return []string{
{{- range .}}
		{{.VarName}}.Name,
{{- end}}
	}
}
{{range .}}
func Get{{.Suffix}}Tool() *ToolDefinition {
	return {{.VarName}}
}
{{end}}`))
